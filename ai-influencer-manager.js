class AIInfluencerManager {
    constructor() {
        this.socket = io('http://localhost:3001');
        this.characters = [];
        this.activityFeed = [];
        this.performanceChart = null;
        
        this.init();
    }

    init() {
        this.setupSocketListeners();
        this.setupEventListeners();
        this.loadCharacters();
        this.initializeChart();
        this.startRealTimeUpdates();
    }

    setupSocketListeners() {
        // Connection status
        this.socket.on('connect', () => {
            this.updateConnectionStatus('connected');
            console.log('Connected to Eliza Bridge Service');
        });

        this.socket.on('disconnect', () => {
            this.updateConnectionStatus('disconnected');
            console.log('Disconnected from Eliza Bridge Service');
        });

        // Real-time character updates
        this.socket.on('character-created', (data) => {
            this.addActivityItem('character-created', `New AI influencer "${data.name}" created`, data);
            this.loadCharacters();
        });

        this.socket.on('character-updated', (data) => {
            this.addActivityItem('character-updated', `AI influencer "${data.name}" updated`, data);
            this.updateCharacterInList(data);
        });

        this.socket.on('autonomous-mode-changed', (data) => {
            const status = data.enabled ? 'activated' : 'deactivated';
            this.addActivityItem('autonomous-changed', `Autonomous mode ${status} for character ${data.characterId}`, data);
            this.loadCharacters();
        });

        this.socket.on('content-generated', (data) => {
            this.addActivityItem('content-generated', `New content generated by AI influencer`, data);
            this.updateStats();
        });

        this.socket.on('engagement-activity', (data) => {
            this.addActivityItem('engagement-activity', `Engagement activity: ${data.activity}`, data);
        });

        this.socket.on('trends-updated', (data) => {
            this.addActivityItem('trends-updated', `Trending topics updated for AI influencer`, data);
        });
    }

    setupEventListeners() {
        // Create character modal
        document.getElementById('create-character-btn').addEventListener('click', () => {
            this.showCreateModal();
        });

        document.getElementById('close-modal').addEventListener('click', () => {
            this.hideCreateModal();
        });

        document.getElementById('cancel-create').addEventListener('click', () => {
            this.hideCreateModal();
        });

        document.getElementById('create-character-submit').addEventListener('click', () => {
            this.createCharacter();
        });

        // Quick actions
        document.getElementById('sync-characters').addEventListener('click', () => {
            this.syncCharacters();
        });

        document.getElementById('generate-content').addEventListener('click', () => {
            this.generateContent();
        });

        document.getElementById('bulk-activate').addEventListener('click', () => {
            this.bulkActivate();
        });
    }

    updateConnectionStatus(status) {
        const indicator = document.getElementById('connection-indicator');
        const text = document.getElementById('connection-text');
        
        if (status === 'connected') {
            indicator.className = 'status-indicator status-active';
            text.textContent = 'Connected';
        } else {
            indicator.className = 'status-indicator status-inactive';
            text.textContent = 'Disconnected';
        }
    }

    async loadCharacters() {
        try {
            const response = await fetch('http://localhost:3001/api/characters');
            const data = await response.json();
            
            if (data.success) {
                this.characters = data.characters;
                this.renderCharacters();
                this.updateStats();
            }
        } catch (error) {
            console.error('Error loading characters:', error);
            this.showError('Failed to load characters');
        }
    }

    renderCharacters() {
        const grid = document.getElementById('characters-grid');
        grid.innerHTML = '';

        if (this.characters.length === 0) {
            grid.innerHTML = `
                <div class="col-span-2 text-center py-12">
                    <i class="fas fa-robot text-6xl text-gray-300 mb-4"></i>
                    <h3 class="text-lg font-medium text-gray-900 mb-2">No AI Influencers Yet</h3>
                    <p class="text-gray-500 mb-4">Create your first autonomous AI influencer to get started</p>
                    <button onclick="aiManager.showCreateModal()" class="inline-flex items-center px-4 py-2 border border-transparent rounded-md shadow-sm text-sm font-medium text-white bg-indigo-600 hover:bg-indigo-700">
                        <i class="fas fa-plus mr-2"></i>
                        Create AI Influencer
                    </button>
                </div>
            `;
            return;
        }

        this.characters.forEach(character => {
            const card = this.createCharacterCard(character);
            grid.appendChild(card);
        });
    }

    createCharacterCard(character) {
        const card = document.createElement('div');
        card.className = 'character-card bg-white border border-gray-200 rounded-lg p-6 shadow-sm';
        
        const statusClass = character.autonomous ? 'status-autonomous' : 
                           character.lastActive ? 'status-active' : 'status-inactive';
        const statusText = character.autonomous ? 'Autonomous' : 
                          character.lastActive ? 'Active' : 'Inactive';

        card.innerHTML = `
            <div class="flex items-center justify-between mb-4">
                <div class="flex items-center">
                    <div class="flex-shrink-0">
                        <div class="w-10 h-10 bg-gradient-to-br from-indigo-500 to-purple-600 rounded-full flex items-center justify-center text-white font-bold text-lg">
                            ${character.name.charAt(0).toUpperCase()}
                        </div>
                    </div>
                    <div class="ml-3">
                        <h3 class="text-lg font-medium text-gray-900">${character.name}</h3>
                        <div class="flex items-center text-sm text-gray-500">
                            <span class="status-indicator ${statusClass}"></span>
                            ${statusText}
                        </div>
                    </div>
                </div>
                <div class="flex space-x-2">
                    <button onclick="aiManager.toggleAutonomous('${character.id}')" class="p-2 text-gray-400 hover:text-gray-600">
                        <i class="fas fa-${character.autonomous ? 'pause' : 'play'}"></i>
                    </button>
                    <button onclick="aiManager.viewAnalytics('${character.id}')" class="p-2 text-gray-400 hover:text-gray-600">
                        <i class="fas fa-chart-bar"></i>
                    </button>
                </div>
            </div>
            
            <div class="space-y-3">
                <div class="flex justify-between text-sm">
                    <span class="text-gray-500">Personality:</span>
                    <span class="font-medium text-gray-900">${character.personality || 'Default'}</span>
                </div>
                <div class="flex justify-between text-sm">
                    <span class="text-gray-500">Platforms:</span>
                    <span class="font-medium text-gray-900">${(character.platforms || ['Twitter']).join(', ')}</span>
                </div>
                <div class="flex justify-between text-sm">
                    <span class="text-gray-500">Posts Today:</span>
                    <span class="font-medium text-gray-900">${Math.floor(Math.random() * 10) + 1}</span>
                </div>
                <div class="flex justify-between text-sm">
                    <span class="text-gray-500">Engagement:</span>
                    <span class="font-medium text-green-600">${(Math.random() * 5 + 2).toFixed(1)}%</span>
                </div>
            </div>
            
            <div class="mt-4 pt-4 border-t border-gray-200">
                <div class="flex space-x-2">
                    <button onclick="aiManager.generateContentFor('${character.id}')" class="flex-1 inline-flex items-center justify-center px-3 py-2 border border-gray-300 rounded-md shadow-sm text-sm font-medium text-gray-700 bg-white hover:bg-gray-50">
                        <i class="fas fa-magic mr-1"></i>
                        Generate
                    </button>
                    <button onclick="aiManager.editCharacter('${character.id}')" class="flex-1 inline-flex items-center justify-center px-3 py-2 border border-gray-300 rounded-md shadow-sm text-sm font-medium text-gray-700 bg-white hover:bg-gray-50">
                        <i class="fas fa-edit mr-1"></i>
                        Edit
                    </button>
                </div>
            </div>
        `;

        return card;
    }

    updateStats() {
        const totalInfluencers = this.characters.length;
        const activeAutonomous = this.characters.filter(c => c.autonomous).length;
        const postsToday = this.characters.reduce((sum, c) => sum + (Math.floor(Math.random() * 10) + 1), 0);
        const revenueGenerated = (Math.random() * 1000 + 500).toFixed(2);

        document.getElementById('total-influencers').textContent = totalInfluencers;
        document.getElementById('active-autonomous').textContent = activeAutonomous;
        document.getElementById('posts-today').textContent = postsToday;
        document.getElementById('revenue-generated').textContent = `$${revenueGenerated}`;
    }

    addActivityItem(type, message, data) {
        const timestamp = new Date().toLocaleTimeString();
        const icon = this.getActivityIcon(type);
        
        const activityItem = {
            id: Date.now(),
            type,
            message,
            data,
            timestamp,
            icon
        };

        this.activityFeed.unshift(activityItem);
        
        // Keep only last 20 items
        if (this.activityFeed.length > 20) {
            this.activityFeed = this.activityFeed.slice(0, 20);
        }

        this.renderActivityFeed();
    }

    getActivityIcon(type) {
        const icons = {
            'character-created': 'fas fa-plus-circle text-green-500',
            'character-updated': 'fas fa-edit text-blue-500',
            'autonomous-changed': 'fas fa-bolt text-purple-500',
            'content-generated': 'fas fa-magic text-indigo-500',
            'engagement-activity': 'fas fa-heart text-red-500',
            'trends-updated': 'fas fa-trending-up text-orange-500'
        };
        return icons[type] || 'fas fa-info-circle text-gray-500';
    }

    renderActivityFeed() {
        const feed = document.getElementById('activity-feed');
        feed.innerHTML = '';

        this.activityFeed.forEach(item => {
            const activityElement = document.createElement('div');
            activityElement.className = 'activity-item';
            activityElement.innerHTML = `
                <div class="flex items-start space-x-3">
                    <div class="flex-shrink-0">
                        <i class="${item.icon}"></i>
                    </div>
                    <div class="flex-1">
                        <p class="text-sm text-gray-900">${item.message}</p>
                        <p class="text-xs text-gray-500">${item.timestamp}</p>
                    </div>
                </div>
            `;
            feed.appendChild(activityElement);
        });
    }

    initializeChart() {
        const chartDom = document.getElementById('performance-chart');
        this.performanceChart = echarts.init(chartDom);

        const option = {
            tooltip: {
                trigger: 'axis'
            },
            legend: {
                data: ['Engagement', 'Posts', 'Revenue']
            },
            xAxis: {
                type: 'category',
                data: ['Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat', 'Sun']
            },
            yAxis: {
                type: 'value'
            },
            series: [
                {
                    name: 'Engagement',
                    type: 'line',
                    data: [2.1, 3.2, 2.8, 4.1, 3.5, 2.9, 3.8],
                    smooth: true,
                    itemStyle: { color: '#6366f1' }
                },
                {
                    name: 'Posts',
                    type: 'bar',
                    data: [8, 12, 10, 15, 11, 9, 13],
                    itemStyle: { color: '#10b981' }
                },
                {
                    name: 'Revenue',
                    type: 'line',
                    data: [120, 180, 150, 220, 170, 140, 190],
                    smooth: true,
                    itemStyle: { color: '#f59e0b' }
                }
            ]
        };

        this.performanceChart.setOption(option);
    }

    showCreateModal() {
        document.getElementById('create-character-modal').classList.remove('hidden');
    }

    hideCreateModal() {
        document.getElementById('create-character-modal').classList.add('hidden');
        document.getElementById('create-character-form').reset();
    }

    async createCharacter() {
        const form = document.getElementById('create-character-form');
        const formData = new FormData(form);
        
        const platforms = [];
        form.querySelectorAll('input[name="platforms"]:checked').forEach(checkbox => {
            platforms.push(checkbox.value);
        });

        const characterData = {
            name: formData.get('name'),
            personality: formData.get('personality'),
            platforms: platforms,
            posting_schedule: formData.get('posting_schedule'),
            autonomous: formData.has('autonomous'),
            created: new Date().toISOString()
        };

        try {
            const response = await fetch('http://localhost:3001/api/characters', {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json'
                },
                body: JSON.stringify(characterData)
            });

            const result = await response.json();
            
            if (result.success) {
                this.hideCreateModal();
                this.addActivityItem('character-created', `Successfully created AI influencer "${characterData.name}"`, result.character);
                this.loadCharacters();
                this.showSuccess('AI Influencer created successfully!');
            } else {
                this.showError(result.error || 'Failed to create character');
            }
        } catch (error) {
            console.error('Error creating character:', error);
            this.showError('Failed to create character');
        }
    }

    async toggleAutonomous(characterId) {
        const character = this.characters.find(c => c.id === characterId);
        if (!character) return;

        const enable = !character.autonomous;
        
        try {
            const response = await fetch(`http://localhost:3001/api/characters/${characterId}/activate`, {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json'
                },
                body: JSON.stringify({ autonomous: enable })
            });

            const result = await response.json();
            
            if (result.success) {
                this.showSuccess(`Autonomous mode ${enable ? 'activated' : 'deactivated'} for ${character.name}`);
                this.loadCharacters();
            } else {
                this.showError(result.error || 'Failed to toggle autonomous mode');
            }
        } catch (error) {
            console.error('Error toggling autonomous mode:', error);
            this.showError('Failed to toggle autonomous mode');
        }
    }

    async syncCharacters() {
        this.showInfo('Syncing characters...');
        await this.loadCharacters();
        this.showSuccess('Characters synced successfully!');
    }

    async generateContent() {
        this.showInfo('Generating content for all active influencers...');
        // This would trigger bulk content generation
        setTimeout(() => {
            this.showSuccess('Content generation initiated!');
        }, 2000);
    }

    async bulkActivate() {
        const inactiveCharacters = this.characters.filter(c => !c.autonomous);
        if (inactiveCharacters.length === 0) {
            this.showInfo('All influencers are already active!');
            return;
        }

        this.showInfo(`Activating ${inactiveCharacters.length} influencers...`);
        
        // Activate all inactive characters
        for (const character of inactiveCharacters) {
            await this.toggleAutonomous(character.id);
        }
        
        this.showSuccess('All influencers activated!');
    }

    viewAnalytics(characterId) {
        const character = this.characters.find(c => c.id === characterId);
        if (character) {
            // Redirect to analytics page or show modal
            window.location.href = `dashboard.html?character=${characterId}`;
        }
    }

    editCharacter(characterId) {
        this.showInfo('Edit functionality coming soon!');
    }

    generateContentFor(characterId) {
        this.showInfo('Generating content...');
        // This would trigger content generation for specific character
        setTimeout(() => {
            this.showSuccess('Content generated successfully!');
        }, 1500);
    }

    updateCharacterInList(updatedCharacter) {
        const index = this.characters.findIndex(c => c.id === updatedCharacter.id);
        if (index !== -1) {
            this.characters[index] = updatedCharacter;
            this.renderCharacters();
            this.updateStats();
        }
    }

    startRealTimeUpdates() {
        // Update stats every 30 seconds
        setInterval(() => {
            this.updateStats();
        }, 30000);

        // Refresh activity feed every minute
        setInterval(() => {
            this.renderActivityFeed();
        }, 60000);
    }

    showSuccess(message) {
        this.showNotification(message, 'success');
    }

    showError(message) {
        this.showNotification(message, 'error');
    }

    showInfo(message) {
        this.showNotification(message, 'info');
    }

    showNotification(message, type) {
        // Create notification element
        const notification = document.createElement('div');
        notification.className = `fixed top-20 right-4 z-50 p-4 rounded-lg shadow-lg max-w-sm transition-all duration-300 transform translate-x-full`;
        
        const colors = {
            success: 'bg-green-500 text-white',
            error: 'bg-red-500 text-white',
            info: 'bg-blue-500 text-white'
        };
        
        notification.className += ` ${colors[type]}`;
        notification.innerHTML = `
            <div class="flex items-center">
                <i class="fas fa-${type === 'success' ? 'check' : type === 'error' ? 'times' : 'info'} mr-2"></i>
                <span>${message}</span>
            </div>
        `;
        
        document.body.appendChild(notification);
        
        // Animate in
        setTimeout(() => {
            notification.classList.remove('translate-x-full');
        }, 100);
        
        // Remove after 3 seconds
        setTimeout(() => {
            notification.classList.add('translate-x-full');
            setTimeout(() => {
                document.body.removeChild(notification);
            }, 300);
        }, 3000);
    }
}

// Initialize the AI Influencer Manager
const aiManager = new AIInfluencerManager();